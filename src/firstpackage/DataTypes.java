package firstpackage;

public class DataTypes {

    public static void main(String[] args) {

        String name = "Shadman";
        System.out.println(name);

        int number1 = 10;
        int number2 = 20;

        int resultFromNumbers = number1 + number2;
        System.out.println(number1 + number2);

        String testNumber1 = "10";
        String testNumber2 = "20";

        String resultNumber = testNumber1 + testNumber2;
        System.out.println(resultNumber);

        System.out.println(testNumber1 + testNumber2);

        System.out.println(testNumber1 + resultFromNumbers);

        int number3 = 100;
        int number4 = 50;
        System.out.println(number3 - number4);

        int result = number3 - number4;

        System.out.println(result);

        System.out.println(result * 12);

    }

}


